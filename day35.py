# Python Lamba
# shortcut for definig functions
# lambda is a small anonymous function
x = lambda a : a + 10
# a is the input argument
# x is what the returned value will be assigned to
# a + 10 is the inside of the function

y = lambda f,j,k : f*j*k
print('x function ', x(20))
print('y function ', y(2,3,4))

